    public static void main(String args[]) throws InterruptedException {
        int numanimales = 20, ancho = 1000, alto = 720;

        Generador f = new Generador();
        f.setSize(ancho, alto);
        f.setTitle("Practica 7 PCD");
        f.setLocation(500, 100);

        CanvasComedero cc = new CanvasComedero(ancho, alto);
        f.add(cc);
        f.setVisible(true);

        Thread[] animal = new Thread[numanimales];
        Gato g;
        Comedero c = new Comedero();
        Random rnd = new Random();
        rnd.setSeed(System.currentTimeMillis());
        try {
            for (int i = 0; i < numanimales; i++) {
                if (rnd.nextInt(10) > 4) {
                    animal[i] = new Perro(c, cc);
                    animal[i].start();
                } else {
                    g = new Gato(c, cc);
                    animal[i] = new Thread(g);
                    animal[i].start();
                }
                Thread.sleep((rnd.nextInt(1) + 1) * 1000);
            }
            for (int i = 0; i < numanimales; i++) {
                animal[i].join();
            }
        } catch (InterruptedException ex) {
            java.util.logging.Logger.getLogger(Generador.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);

        }
        System.exit(0);
    }


